#include <iostream>
using namespace std;

int main() {
    int num = 10;       // 정수형 변수
    double pi = 3.14;   // 실수형 변수
    char grade = 'A';   // 문자형 변수
    bool isPassed = true; // 논리형 변수
    string name = "John"; // 문자열 변수

    cout << "숫자: " << num << endl;
    cout << "원주율: " << pi << endl;
    cout << "학점: " << grade << endl;
    cout << "합격 여부: " << isPassed << endl;
    cout << "이름: " << name << endl;



    return 0;
}