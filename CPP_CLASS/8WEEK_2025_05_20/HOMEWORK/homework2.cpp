#include <iostream>
#include <cmath>
using namespace std;

int main() {
    cout << "pow(5.0, 3): " << pow(5.0, 3) << ", pow(5, -3): " << pow(5, -3) << "/n";
    cout << "sqrt(44.56): " << sqrt(44.56) << "/n";
    cout << "exp(-6.2): " << exp(-6.2) << ", exp(44.26): " << exp(44.26) << "/n";
    cout << "log(16.2): " << log(16.2) << ", log10(14.24): " << log10(14.24) << "/n";
    return 0;
}