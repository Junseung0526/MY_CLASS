#include <iostream>
#include <cmath>
using namespace std;

int main() {
    cout << "abs(25): " << abs(25) << ", abs(-23): " << abs(-23) << "/n";
    cout << "floor(44.56): " << floor(44.56) << ", floor(-23.78): " << floor(-23.78) << "/n";
    cout << "ceil(25.23): " << ceil(25.23) << ", ceil(-2.89): " << ceil(-2.89) << "/n";
    return 0;
}