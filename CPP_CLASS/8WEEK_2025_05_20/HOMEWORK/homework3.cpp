#include <iostream>
#include <cmath>
using namespace std;

int main() {
	const double PI = acos(-1);

	cout << "sin(PI): " << sin(PI) << "/n";
	cout << "cos(PI): " << cos(PI) << "/n";
	cout << "tan(1): " << tan(1) << "/n";

	cout << "asin(0.3): " << asin(0.3) << "/n";
	cout << "acos(0.1): " << acos(0.1) << "/n";
	cout << "atan(1): " << atan(1) << "/n";

	return 0;
}
